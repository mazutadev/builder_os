"""
Исключения для модуля управления контейнерами.
"""


class ContainerError(Exception):
    """
    Базовое исключение для операций с контейнерами.

    Все остальные исключения модуля наследуются от этого класса,
    что позволяет легко отлавливать все ошибки, связанные с контейнерами.
    """
    pass


class ContainerCreateError(ContainerError):
    """
    Исключение, возникающее при ошибке создания контейнера.

    Примеры:
        - Отсутствует Docker демон
        - Не удалось загрузить образ
        - Недостаточно прав
    """
    pass


class ContainerStartError(ContainerError):
    """
    Исключение, возникающее при ошибке запуска контейнера.

    Примеры:
        - Контейнер уже запущен
        - Конфликт портов
        - Проблемы с монтированием томов
    """
    pass


class ContainerStopError(ContainerError):
    """
    Исключение, возникающее при ошибке остановки контейнера.

    Примеры:
        - Контейнер уже остановлен
        - Контейнер не отвечает
        - Таймаут при остановке
    """
    pass


class ContainerExecuteError(ContainerError):
    """
    Исключение, возникающее при ошибке выполнения команды в контейнере.

    Примеры:
        - Команда завершилась с ненулевым кодом
        - Контейнер не запущен
        - Таймаут выполнения команды
    """
    pass


class ContainerNotFoundError(ContainerError):
    """
    Исключение, возникающее когда контейнер не найден.

    Примеры:
        - Попытка работы с несуществующим контейнером
        - Контейнер был удален
    """
    pass


class ContainerConfigError(ContainerError):
    """
    Исключение, возникающее при ошибках в конфигурации контейнера.

    Примеры:
        - Неподдерживаемая операционная система
        - Некорректная версия ОС
        - Неверный формат настроек
    """
    pass
